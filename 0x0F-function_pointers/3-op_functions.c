#include "3-calc.h"

/**
 * op_add - adds two numbers
 * @a: number
 * @b: number
 *
 * Return: result of the addition
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtrscts two numbers
 * @a: number
 * @b: number
 *
 * Return: result of the subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_div - divides two numbers
 * @a: number
 * @b: number
 *
 * Return: result of the division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - find the reminder when @a is divided by @b
 * @a: number
 * @b: number
 *
 * Return: result of the modulus division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
/**
 * op_mul - multiply two numbers
 * @a: number
 * @b: number
 *
 * Return: result of the mulitiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
